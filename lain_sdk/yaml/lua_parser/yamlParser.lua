--- lua's copy a table
---------------------------------------------------------------------------------------------------------------
function copyTab(st)
	if type(st) ~= "table" then
		return st
	end

    local tab = {}
    for k, v in pairs(st or {}) do
        if type(v) ~= "table" then
            tab[k] = v
        else
            tab[k] = copyTab(v)
        end
    end
    return tab
end



